<script setup>
import { ref } from 'vue';
import { useNoteStore } from '../store/note';

const title = ref("");
const noteStore = useNoteStore();

const handleSubmit = () => {
    noteStore.addNote(title.value);
    title.value = "";
}
</script>

<template>
    <form class="note-form" @submit.prevent="handleSubmit">
        <input
            type="text"
            class="note-title"
            placeholder="Nueva nota"
            v-model="title"
        />
        <button class="create-btn">+</button>
    </form>
</template>

